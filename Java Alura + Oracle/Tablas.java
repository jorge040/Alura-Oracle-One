public class Tablas {
    public static void main(String[] args) {
        int total;
       for (int i = 1; i <= 10; i++) {
        for (int j = 1; j <= 10; j++) {
            total = i * j;
            System.out.println("");
            System.out.print(i + " Por " +  j + " Igual: " + total);  
        }
        System.out.println("");
       }
       
    }
}
